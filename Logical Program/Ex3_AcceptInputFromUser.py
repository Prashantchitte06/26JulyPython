# 3: Accept input from user
# In Python, input() is a built-in function used to get input from the user during program execution.


#Accept any type of input or String input
user_input = input("Enter something: ")
print("You entered:", user_input)


#Accept int input
num = int(input("Enter a number: "))
print("You entered number:", num)


#Accept float input
price = float(input("Enter the price: "))
print("Price entered:", price)